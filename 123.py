a = input("請輸入您的身高:")
b = input("請輸入您的體重")
a = float(a)
b = float(b)
c = b / (a / 100)**2
if c <= 18.5:
    print("您的身高為%.1f公分，您的體重為%.1f公斤，您的BMI值為%.2f，您的體重屬於%s。" %(a, b, c, "過輕"))
elif c > 25:
    print("您的身高為%.1f公分，您的體重為%.1f公斤，您的BMI值為%.2f，您的體重屬於%s。" % (a, b, c, "過重"))
else:
    print("您的身高為%.1f公分，您的體重為%.1f公斤，您的BMI值為%.2f，您的體重屬於%s。" % (a, b, c, "標準"))
